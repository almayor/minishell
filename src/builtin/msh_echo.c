#include "minishell.h"

int		msh_echo(char *const *argv)
{
	size_t i;

	i = 1;
	while (argv[i])
	{
		ft_printf("%s%c", argv[i], argv[i + 1] ? ' ' : '\n');
		i++;
	}
	return (1);
}
